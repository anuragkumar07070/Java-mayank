public class literals {
    public static void main(String[] args) {
        byte age = 34;
        int age2 = 56;
        char ch = 'A';
        float f1 = 5.6f;
        double d1 = 5.6;
        
        System.out.println(age);
        System.out.println(age2);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d1);
    }
}
