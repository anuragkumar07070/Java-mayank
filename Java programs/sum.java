public class sum {
    public static void main(String[] args) {
        System.out.print("Sum of three Numbers: ");   // "print" (to print in single line) & "println" (to print and also give an extra line)
        int num1 = 6;
        int num2 = 8;
        int num3 = 10;

        int sum = num1 + num2 + num3;
        System.out.println(sum);
    }
}
