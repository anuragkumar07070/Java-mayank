public class if_else {
    public static void main(String[] args) {
        int age = 19;

        /* 

        if (age > 18){
            System.out.println("Yes, you can drive");
        }
        else{
            System.out.println("No, you cannot drive");
        }

        */

        // OR

        boolean cond = (age > 18);

        if (cond){
            System.out.println("Yes, you can drive");
        }
        else{
            System.out.println("No, you cannot drive");
        }

        /*
         * LOGICAL OPERATORS:
         * 
         * AND -> &&
         * OR -> ||
         * NOT -> !
         * 
         */
    }
}
